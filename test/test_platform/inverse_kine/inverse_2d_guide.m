function update_robot(handles)
axes(handles.axes1);
hold off;
[theta_2,theta_1]=inverse_3d_kinematic([str2double(get(handles.edit7,'String')) str2double(get(handles.edit8,'String'))],[str2double(get(handles.edit1,'String')) str2double(get(handles.edit2,'String'))],[str2double(get(handles.edit5,'String')) str2double(get(handles.edit6,'String'))]);
set(handles.edit13,'String',strcat(num2str(theta_1(1)),"/",num2str(theta_2(1))));
set(handles.edit14,'String',strcat(num2str(theta_1(2)),"/",num2str(theta_2(2))));
[theta_2,theta_1]=inverse_3d_kinematic([str2double(get(handles.edit11,'String')) str2double(get(handles.edit12,'String'))],[str2double(get(handles.edit3,'String')) str2double(get(handles.edit4,'String'))],[str2double(get(handles.edit5,'String')) str2double(get(handles.edit6,'String'))]);
set(handles.edit15,'String',strcat(num2str(theta_1(1)),"/",num2str(theta_2(1))));
set(handles.edit16,'String',strcat(num2str(theta_1(2)),"/",num2str(theta_2(2))));
axis([-20 20 -5 20 0 20])
plot3([-20 20],[0 0],[0 0],'r');
plot3([0 0],[-5 20],[0 0],'b');
plot3([0 0],[0 0],[0 20],'g');

